int		ft_button(int i, int j, int k)
{
	if (((i <= j) && (k >= j)) || ((i >= j) && (k <= j)))
		return (j);
	else if (((i >= j) && (i <= k)) || ((i <= j) && (i >= k)))
		return (i);
	else
		return (k);
}
