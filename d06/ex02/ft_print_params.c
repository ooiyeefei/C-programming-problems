int		ft_putchar(char c);

int		main(int argc, char **argv)
{
	while (argc > 0)
	{
		while (**argv != '\0')
		{
			ft_putchar(**argv);
			(*argv)++;
		}
		ft_putchar('\n');
		(**argv)++;
		argc--;
	}
	return (0);
}
