#define ALPHA(x)(((x>='A')&&(x<='Z'))||((x>='a')&&(x<='z')))

int		ft_strlen(char *str)
{
	int count;

	count = 0;
	while (*str != '\0')
	{
		if (ALPHA(*str))
			count++;
		str++;
	}
	return (count);
}
