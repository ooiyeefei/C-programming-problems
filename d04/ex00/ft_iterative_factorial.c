int		ft_iterative_factorial(int nb)
{
	int ans;

	ans = nb;
	if (nb <= 0)
	{
		if (nb == 0)
			ans = 1;
		else
			ans = 0;
	}
	while (nb > 1)
	{
		ans = ans * (nb - 1);
		nb--;
	}
	return (ans);
}
