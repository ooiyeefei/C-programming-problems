int		ft_is_prime(int nb)
{
	int a;
	int count;

	a = 2;
	count = 0;
	if (nb <= 1)
		return (0);
	while (a < nb)
	{
		if ((nb % a) == 0)
		{
			count++;
		}
		a++;
	}
	if (count == 0)
		return (1);
	else
		return (0);
}
