#include "ft.h"

void	rush03(int x, int y, char *str)
{
	int		i;
	int		j;
	int		k;

	j = 0;
	k = 0;
	while (j < y)
	{
		i = 0;
		while (i < x)
		{
			if (i == 0)
				str[k] = (j == 0 || j == (y - 1)) ? 'A' : 'B';
			else if (i == (x - 1))
				str[k] = (j == 0 || j == (y - 1)) ? 'C' : 'B';
			else
				str[k] = (j == 0 || j == (y - 1)) ? 'B' : ' ';
			i++;
			k++;
		}
		last_value(str, &k, &j);
	}
	str[k] = '\0';
}
