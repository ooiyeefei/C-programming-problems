void	last_value(char *str, int *k, int *j)
{
	str[*k] = '\n';
	*j = *j + 1;
	*k = *k + 1;
}

void	increment(int *i, int *k)
{
	*i = *i + 1;
	*k = *k + 1;
}

void	init_value(int *k, int *j)
{
	*k = 0;
	*j = 0;
}

