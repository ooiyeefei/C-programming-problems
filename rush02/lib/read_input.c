#include "ft.h"

void	read_input(char *input, int *x, int *y)
{	
	char 	c;
	int		i;

	i = 0;
	while (read(0, &c, 1))
	{
		input[i] = c;
		if (c == '\n')
			(*y)++;
		else
			(*x)++;
		i++;
	}
	input[i] = '\0';
	*x = *x / *y;	
}
