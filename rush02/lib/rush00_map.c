#include "ft.h"

void	rush00(int x, int y, char *str)
{
	int		i;
	int		j;
	int		k;

	k = 0;
	j = 0;
	while (j < y)
	{
		i = 0;
		while (i < x)
		{
			if (i == 0 || i == (x - 1))
				str[k] = (j == 0 || j == (y - 1)) ? 'o' : '|';
			else
				str[k] = (j == 0 || j == (y - 1)) ? '-' : ' ';
			increment(&i, &k);
		}
		last_value(str, &k, &j);
	}
	str[k] = '\0';
}

