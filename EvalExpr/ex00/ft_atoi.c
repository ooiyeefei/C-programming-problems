#include "ft.h"

int		ft_atoi(char *str)
{
	int i;
	int neg;
	int ans;

	i = 0;
	neg = 1;
	ans = 0;
	while (WHSP(str[i]))
		i++;
	if ((str[i] == '+') || (str[i] == '-'))
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (NUM(str[i]))
	{
		ans = ans * 10 + str[i] - '0';
		i++;
	}
	return (ans * neg);
}
