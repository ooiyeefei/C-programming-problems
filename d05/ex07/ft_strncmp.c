int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned char s1r;
	unsigned char s2r;

	if (n == 0)
		return (0);
	while ((*s1 || *s2) && n > 0)
	{
		if (*s1 != *s2)
		{
			s1r = *s1;
			s2r = *s2;
			return (s1r - s2r);
		}
		s1++;
		s2++;
		n--;
	}
	return (0);
}
